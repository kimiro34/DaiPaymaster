import { ethers, BigNumberish } from "ethers";
import { keccak256, defaultAbiCoder, solidityPack } from "ethers/lib/utils.js";
import { ecsign } from "ethereumjs-util";

const getPermitDigest = (
  approve: {
    owner: string;
    spender: string;
    value: BigNumberish;
  },
  nonce: BigNumberish,
  deadline: BigNumberish,
  allowed: Boolean
) => {
  const DOMAIN_SEPARATOR =
    "0x16c71d5e724b4895d1eda159ed52657b98b9383418cdf9872697474064bbc728";
  const PERMIT_TYPEHASH =
    "0xea2aa0a1be11a07ed86d755c93467f4f82362b452371d1ba94d1715123511acb";
  return keccak256(
    solidityPack(
      ["bytes1", "bytes1", "bytes32", "bytes32"],
      [
        "0x19",
        "0x01",
        DOMAIN_SEPARATOR,
        keccak256(
          defaultAbiCoder.encode(
            ["bytes32", "address", "address", "uint256", "uint256", "bool"],
            [
              PERMIT_TYPEHASH,
              approve.owner,
              approve.spender,
              nonce,
              deadline,
              allowed,
            ]
          )
        ),
      ]
    )
  );
};

export const sign = (digest: any, privateKey: any) => {
  return ecsign(Buffer.from(digest.slice(2), "hex"), privateKey);
};

